    private void loadUsersComboBox() {
    jComboBox2.removeAllItems();
    comboItemsMap.clear();
    
    if (userType.equals("Lecturer")) {
        // Для Student и Lecturer загружаем по курсу
        Map<String, User> users = CourseAdministrator.getUsersByCourseAndType(selectedCourse, userType);
        
        if (users.isEmpty()) {
            jComboBox2.addItem("No " + userType + "s available for " + selectedCourse);
            return;
        }
        
        for (User user : users.values()) {
            String displayText = CourseAdministrator.formatUserDisplay(user);
            jComboBox2.addItem(displayText);
            comboItemsMap.put(displayText, user);
        }
    } else if (userType.equals("Student")) {
        // Для Student и Lecturer загружаем по курсу
        Map<String, User> users = CourseAdministrator.getListOfStudentsForSubject(selectedCourse);
        
        if (users.isEmpty()) {
            jComboBox2.addItem("No " + userType + "s available for " + selectedCourse);
            return;
        }
        
        for (User user : users.values()) {
            String displayText = CourseAdministrator.formatUserDisplay(user);
            jComboBox2.addItem(displayText);
            comboItemsMap.put(displayText, user);
        }
    } else if (userType.equals("AcademicOfficer") || userType.equals("CourseAdministrator")) {
        // Для AcademicOfficer и CourseAdministrator загружаем всех (они не привязаны к курсу)
        Map<String, User> users = CourseAdministrator.getUsersByType(userType);
        
        if (users.isEmpty()) {
            jComboBox2.addItem("No " + userType + "s available");
            return;
        }
        
        for (User user : users.values()) {
            String displayText = user.getName() + " (" + user.getEmail() + ")";
            jComboBox2.addItem(displayText);
            comboItemsMap.put(displayText, user);
        }
    }
    
    if (jComboBox2.getItemCount() > 0) {
        jComboBox2.setSelectedIndex(0);
    }
}
    
    
    
    private String buildConfirmationMessage(User user) {
        StringBuilder sb = new StringBuilder();
        sb.append("<html><b>⚠️ Are you sure you want to delete this user?</b><br><br>");
        sb.append("<b>User Details:</b><br>");
        sb.append("• Name: ").append(user.getName()).append("<br>");
        sb.append("• Email: ").append(user.getEmail()).append("<br>");
        sb.append("• Type: ").append(user.getTypeOfUser()).append("<br>");
        
        if (user instanceof Student) {
            Student s = (Student) user;
            sb.append("• Course: ").append(s.getMajor()).append("<br>");
            sb.append("• Year: ").append(s.getYear()).append("<br>");
        } else if (user instanceof Lecturer) {
            Lecturer l = (Lecturer) user;
            sb.append("• Assigned Course: ").append(l.getAssignedCourse()).append("<br>");
        }
        
        sb.append("<br><b>⚠️ Warning:</b> This action cannot be undone!</html>");
        return sb.toString();
    }

