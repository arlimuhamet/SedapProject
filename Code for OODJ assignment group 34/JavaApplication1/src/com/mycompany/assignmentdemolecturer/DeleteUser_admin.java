    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
       String selectedText = (String) jComboBox2.getSelectedItem();
    
    if (selectedText == null || selectedText.startsWith("No ")) {
        JOptionPane.showMessageDialog(this, 
            "Please select a user to delete", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Получаем пользователя
    User userToDelete = comboItemsMap.get(selectedText);
    if (userToDelete == null) {
        JOptionPane.showMessageDialog(this, 
            "Selected user not found", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    // Подтверждение удаления с подробной информацией
    String confirmationMessage = buildConfirmationMessage(userToDelete);
    
    int confirmation = JOptionPane.showConfirmDialog(
        this, 
        confirmationMessage, 
        "⚠️ Confirm User Deletion", 
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE
    );
    
    if (confirmation == JOptionPane.YES_OPTION) {
        try {
            // Для AcademicOfficer и CourseAdministrator используем пустой курс
            String courseForDeletion = selectedCourse;
            if (userType.equals("AcademicOfficer") || userType.equals("CourseAdministrator")) {
                courseForDeletion = "";
            }
            
            // Используем CourseAdministrator для удаления пользователя
            boolean success = CourseAdministrator.deleteUser(
                userToDelete.getEmail(), 
                courseForDeletion, 
                userType
            );
            
            if (success) {
                String successMessage = "✅ User deleted successfully!\n\n" +
                    "Name: " + userToDelete.getName() + "\n" +
                    "Email: " + userToDelete.getEmail() + "\n" +
                    "Type: " + userToDelete.getTypeOfUser();
                
                // Добавляем информацию о курсе только для Student и Lecturer
                if (userType.equals("Student") || userType.equals("Lecturer")) {
                    successMessage += "\nCourse: " + selectedCourse;
                }
                
                JOptionPane.showMessageDialog(this, 
                    successMessage,
                    "Success", 
                    JOptionPane.INFORMATION_MESSAGE);
                
                // Обновляем комбобокс
                loadUsersComboBox();
            } else {
                JOptionPane.showMessageDialog(this, 
                    "❌ Failed to delete user", 
                    "Error", 
                    JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "❌ Error deleting user: " + e.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_jButton6ActionPerformed

