        public void updateUser() {     
    try {
        String email = CurrentUser.getEmail();
        String newName = newNameField.getText().trim();
        String newPassword = newPasswordField.getText().trim();

        if (newName.isEmpty()) {
            newName = CurrentUser.getName();
        }
        if (newPassword.isEmpty()) {
            newPassword = CurrentUser.getPassword();
        }

        if (newName.matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(null, "Name cannot contain numbers!");
            return;
        }

        Map<String, User> userMap = userRepo.getUserMap();
        User oldUser = userMap.get(email);

        if (oldUser == null) {
            JOptionPane.showMessageDialog(null, "User with this email was not found!");
            return;
        }

        String typeOfUser = oldUser.getTypeOfUser();

        switch (typeOfUser) {
            case "Lecturer":
                Lecturer oldLect = (Lecturer) oldUser;
                Lecturer newLect = new Lecturer(
                    oldLect.getEmail(),
                    newName,
                    newPassword,
                    oldLect.getAssignedCourse()
                );
                userMap.put(email, newLect);
                break;

            case "Student":
                Student oldStud = (Student) oldUser;
                Student newStud = new Student(
                    oldStud.getEmail(),
                    newName,
                    newPassword,
                    oldStud.getMajor(),
                    oldStud.getYear()
                );
                userMap.put(email, newStud);
                break;

            case "CourseAdministrator":
                CourseAdministrator oldCA = (CourseAdministrator) oldUser;
                CourseAdministrator newCA = new CourseAdministrator(
                    oldCA.getEmail(),
                    newName,
                    newPassword
                );
                userMap.put(email, newCA);
                break;

            case "AcademicOfficer":
                AcademicOfficer oldAO = (AcademicOfficer) oldUser;
                AcademicOfficer newAO = new AcademicOfficer(
                    oldAO.getEmail(),
                    newName,
                    newPassword
                );
                userMap.put(email, newAO);
                break;

            default:
                JOptionPane.showMessageDialog(null, "Unknown user type: " + typeOfUser);
                return;
        }

        userRepo.setUserMap(userMap);
        userRepo.saveToFile(USERS_FILE);
        currentNameField.setText(newName);
        currentPasswordField.setText(newPassword);
        CurrentUser.set(CurrentUser.getEmail(), newName, newPassword, CurrentUser.getTypeOfUser());

        JOptionPane.showMessageDialog(null, "User data has been successfully updated!");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error updating user: " + e.getMessage());
        e.printStackTrace();
    }
}

