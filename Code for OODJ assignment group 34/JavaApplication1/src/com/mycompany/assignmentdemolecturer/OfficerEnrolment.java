    private void loadAtRiskStudents() {
    try {        
        DefaultTableModel model = (DefaultTableModel) TableStudent.getModel();
        model.setRowCount(0);
        
        for (String email : recordsRepo.getRecordsMap().keySet()) {
            Map<String, AcademicRecordItem> courses = recordsRepo.getRecordsMap().get(email);
            
            double totalGPA = 0;
            int courseCount = 0;
            int failedCourses = 0;
            
            for (AcademicRecordItem course : courses.values()) {
                double gpa = course.getGPA();
                totalGPA += gpa;
                courseCount++;
                if (gpa < 2.0) failedCourses++;
            }
            
            double cgpa = courseCount > 0 ? totalGPA / courseCount : 0;
            
            if (cgpa < 2.5 || failedCourses >= 1) {
                User user = userRepo.getUser(email);
                String name = user != null ? user.getName() : email;
                String program = "Unknown";
                String level = "Unknown";
                
                if (user instanceof Student) {
                    Student student = (Student) user;
                    program = student.getMajor();
                    level = student.getYear();
                }
                
                model.addRow(new Object[]{
                    name, email, 
                    program + " - Year " + level,
                    program + " - Year " + getNextLevel(level),
                    String.format("%.2f", cgpa),
                    "Pending"
                });
            }
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading students");
    }
}

private String getNextLevel(String currentLevel) {
    try {
        int currentYear = Integer.parseInt(currentLevel);
        if (currentYear >= 1 && currentYear <= 3) {
            return String.valueOf(currentYear + 1); // 1→2, 2→3, 3→4
        }
        if (currentYear == 4) {
            return "Graduate"; // Year 4 becomes Graduate
        }
    } catch (NumberFormatException e) {
        
    }
    return "Next Level";
}

