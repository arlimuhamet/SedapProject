    private void ButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSubmitActionPerformed
        // TODO add your handling code here:
        int selectedRow = TableStudent.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student first.");
        return;
    }
    
    String decision = (String) ComboBoxDecision.getSelectedItem();
    String studentEmail = (String) TableStudent.getValueAt(selectedRow, 1);
    String studentName = (String) TableStudent.getValueAt(selectedRow, 0);
    
    try {
        if ("Accept".equals(decision)) {
            // CHECK CGPA BEFORE ACCEPTING
            String cgpaStr = (String) TableStudent.getValueAt(selectedRow, 4);
            double studentCGPA = Double.parseDouble(cgpaStr);

            if (studentCGPA < 2.0) {
                JOptionPane.showMessageDialog(this, 
                    "Cannot accept " + studentName + "!\n" +
                    "CGPA: " + cgpaStr + " is below 2.0\n" +
                    "Only 'Decline' is allowed for low GPA students.",
                    "Error", JOptionPane.ERROR_MESSAGE);
                return; // STOP HERE - Don't accept
            }

            User user = userRepo.getUser(studentEmail);
            if (user instanceof Student) {
                Student student = (Student) user;
                student.setYear(getNextLevel(student.getYear()));
                userRepo.saveToFile(USERS_FILE);
                TableStudent.setValueAt("Approved", selectedRow, 5);
                JOptionPane.showMessageDialog(this, studentName + " promoted!");
        }
    } else if ("Decline".equals(decision)) {
            recordsRepo.getRecordsMap().remove(studentEmail);
            recordsRepo.saveToFile(ACADEMIC_RECORDS_FILE);
            DefaultTableModel model = (DefaultTableModel) TableStudent.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, studentName + " declined.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    
    }//GEN-LAST:event_ButtonSubmitActionPerformed

