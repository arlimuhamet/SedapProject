    private void ButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSubmitActionPerformed
        // TODO add your handling code here:
        String notificationType = (String) ComboBoxNotType.getSelectedItem();
    String recipient = (String) ComboBoxTo.getSelectedItem();
    String subject = TextFieldSubject.getText().trim();
    String message = TextAreaMessage.getText().trim();
    
    // Validation
    if (notificationType == null || notificationType.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a notification type!", 
            "Validation Error", JOptionPane.WARNING_MESSAGE);
        ComboBoxNotType.requestFocus();
        return;
    }
    
    if (recipient == null || recipient.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a recipient!", 
            "Validation Error", JOptionPane.WARNING_MESSAGE);
        ComboBoxTo.requestFocus();
        return;
    }
    
    if (subject.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a subject!", 
            "Validation Error", JOptionPane.WARNING_MESSAGE);
        TextFieldSubject.requestFocus();
        return;
    }
    
    if (message.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a message!", 
            "Validation Error", JOptionPane.WARNING_MESSAGE);
        TextAreaMessage.requestFocus();
        return;
    }
    
    try {
        // Send real email
        AcademicOfficer userObj = (AcademicOfficer) userRepo.getUserMap().get(CurrentUser.getEmail());
        userObj.sendEmailToStudent(recipient, subject, message);
        
        JOptionPane.showMessageDialog(this, 
            "✅ Email sent successfully!\n\n" +
            "To: " + recipient + "\n" +
            "Subject: " + subject, 
            "Success", JOptionPane.INFORMATION_MESSAGE);
        
        // Clear form
        TextFieldSubject.setText("");
        TextAreaMessage.setText("");
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, 
            "❌ Error: " + e.getMessage() + 
            "\n\nCheck:\n1. Internet connection\n2. Email credentials\n3. JavaMail JAR files", 
            "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
        }
    
    }//GEN-LAST:event_ButtonSubmitActionPerformed

