    private void initializeNotificationData() {
        try {

            // Set notification types in combo box
            ComboBoxNotType.setModel(new javax.swing.DefaultComboBoxModel<>(
                new String[] { "Academic Warning", "Enrolment Status", "Meeting Invitation", "General Announcement" }
            ));
            
            // Load all student emails into "To" combo box
            List<String> studentEmails = new ArrayList<>();
            for (User user : userRepo.getUserMap().values()) {
                if (user instanceof Student) {
                    studentEmails.add(user.getEmail());
                }
            }
            
            // Add "All Students" option at the beginning
            studentEmails.add(0, "All Students");
            
            ComboBoxTo.setModel(new javax.swing.DefaultComboBoxModel<>(
                studentEmails.toArray(new String[0])
            ));
            
            // Clear default text
            TextFieldSubject.setText("");
            TextAreaMessage.setText("");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }

